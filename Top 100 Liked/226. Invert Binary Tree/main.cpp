struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode(int x) : val(x), left(NULL), right(NULL) {}
};

class Solution {
public:
    TreeNode* invertTree(TreeNode* root) {
        if (root == NULL) return NULL;
        root->right = invertTree(root->right);
        root->left = invertTree(root->left);
        swap(root->left, root->right);
        return root;
    }
};

